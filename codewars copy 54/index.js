//Volume of a Cuboid

/*
    Бобу нужен быстрый способ рассчитать объем кубоида с тремя значениями: 
    длиной, шириной и высотой кубоида.
    Напишите функцию, которая поможет Бобу с этим расчетом.
*/

class Kata {
    static getVolumeOfCuboid(length, width, height) {
        return length * width * height;
    }
}

